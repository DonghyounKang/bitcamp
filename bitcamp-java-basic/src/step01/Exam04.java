//리터럴(literal)
package step01;
public class Exam04{
    public static void main(String[] args) {
        //숫자: 그대로 적기
        System.out.println(10);
        System.out.println(+10);
        System.out.println(-10);
        System.out.println(3.14);

        //문자열: ""
        System.out.println("오호라");

        //한 개의 문자: ''
        System.out.println('가');

        //논리값: 대소문자 구분할 것
        System.out.println(true);
        System.out.println(false);
    }
}

//자바코드로 값을 표현(literal)하는 것

