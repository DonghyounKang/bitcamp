        //   그 생성자의 첫번째 문장이 수퍼 클래스의 생성자를 호출하는 명령
        //   따라서 수퍼클래스인 A클래스의 생성자를 호출
        //3) A클래스 생성자 호출
        //   그 생성자의 첫번째 문장이 수퍼 클래스의 생성자를 호출하는 명령
        //   그래서 수퍼클래스의 Object의 생성자를 호출
