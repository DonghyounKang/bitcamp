//관계 연산자 - <, <=, >, >=, ==, !=
package step04;

public class Exam02_1{
    public static void main(String[] args){

        int a = 10;
        int b = 20;

        boolean r1 = a < b; // true
        //int r2 = a < b; // Compile error

        System.out.println(a < b);
        System.out.println(a <= b);
        System.out.println(a > b);
        System.out.println(a >= b);
        System.out.println(a == b);
        System.out.println(a != b);


    }
}

